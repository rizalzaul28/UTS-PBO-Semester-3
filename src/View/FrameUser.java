    private void tb_UserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_UserMouseClicked
        int baris = tb_User.rowAtPoint(evt.getPoint());
        String Username = tb_User.getValueAt(baris, 0).toString();
        String Email = tb_User.getValueAt(baris, 1).toString();
        String Fullname = tb_User.getValueAt(baris, 2).toString();
        String Status = tb_User.getValueAt(baris, 3).toString();

        tf_Username.setText(Username);
        tf_Email.setText(Email);
        tf_Fullname.setText(Fullname);
        cb_Status.setSelectedItem(Status);

        tf_Username.setEditable(true);
    }//GEN-LAST:event_tb_UserMouseClicked

